	  		<p class="classic_paragraph">Жизнь школы и поселка тесно связаны. Люди, живущие в поселке, делают его историю, а началом всего является школа.  В настоящее время в Марийском поселении школа является самым крупным объектом и центром распространения грамотности.  </p>
	  		<p class="read_more_target classic_paragraph">Ульяновская Земско-Заводская школа была учреждена в 1871 году, хотя называются и другие даты основания школы, а именно - 1865 и 1875 годы.  Но в  Церковных ведомостях (это архивный материал) можно прочитать: «…Ульяновская Земско-Заводская школа учреждена в 1871 году».</p>
	  		<p class="read_more_target classic_paragraph">В 1875 году школа была преобразована в училище для детей рабочих, которое было открыто в  заводском здании и содержалось на средства попечителя школы П. Ульянова. Училось в нём 53 ученика. </p>
            <p class="read_more_target classic_paragraph">Первыми преподавателями были  Александр Веневицкий, дьяконский сын,  окончивший курс Вятской духовой семинарии (преподаватель Закона Божьего) и Осип Жирнов, крестьянин Шурминского завода, окончивший курс в Вятском Земском училище.  Хозяин завода уделял  большое внимание  тому, чтобы, кроме изучения Закона Божьего, математики, русского языка и истории Отечества, изучались основы стекольного дела. Ученики после учёбы  работали у мастеровых, вникая в азы стекольного производства.   </p>
            <p class="read_more_target classic_paragraph">В 1878 году училище становится трёхклассным.  В октябре 1880 года, обучалось 85 учащихся, в том числе 51 мальчик и 34 девочки.    После Октябрьской революции 1917 года произошли  коренные  изменения  в образовании. Училище было преобразовано  в начальную  школу. </p>
            <p class="read_more_target classic_paragraph">В 1924 году в школе появляется одна из первых в районе организация пионеров «Парижская коммуна» под руководством первого пионервожатого  Александра Михайловича Городилова. Проводились пионерские сборы, вечера на атеистические темы, ставили концерты в посёлке и близлежащих деревнях.</p>
            <p class="read_more_target classic_paragraph">В 1927 году школа становится четырёхклассной, вечерней. Она стала называться ФЗУ (Фабрично-заводским училищем), где осуществлялась профессиональная подготовка будущих мастеров-стеклодувов.</p>
            <p class="read_more_target classic_paragraph">В 1929 году школа становится  семилетней и получает название ФЗС (фабрично-заводская семилетка). Её первым директором был Константин Ильич Попов.  В 1932 году по инициативе учителей  школы прошла первая спартакиада юношей и девушек посёлка, эстафету которой поддержали в районе.</p>
            <p class="read_more_target classic_paragraph">В 1932 году началось строительство новой школы, а в 1934 году в ней уже начались занятия. В 1936 году школа перешла в новое здание и  стала восьмилетней, а в 1940 году  она стала давать полное среднее образование.</p>
            <p class="read_more_target classic_paragraph">Первым директором средней школы был Иван Кузьмич Софронов. 
В 1941 году всё население посёлка провожало многих  выпускников добровольцами на фронт, на защиту Отечества.  Дорого нашей школе обошлась война.  Учителя, которых любили дети, погибли, защищая Родину: Альбицкий С.А., Ведерников А.Н., Елисеев Я.Г., Стариков А.А., Бажнин И.П., Ворончихин А.В., Малинин В.Н., Токарев В.И., Хлебников А.И.  28 выпускников не вернулись с фронта.</p>
            <p class="read_more_target classic_paragraph">В годы войны школа работала и всё делала для развития образования.</p>
            <p class="read_more_target classic_paragraph">Закончилась война. В послевоенные годы, вернувшись с фронта,  директором школы стал В. М. Зверев. Развернулась кипучая школьная жизнь. Возродились и появились новые кружки.</p>
            <p class="read_more_target classic_paragraph">В 1951 году при школе открывается интернат, заведующим которого была Е.С. Журавлёва. </p>
            <p class="read_more_target classic_paragraph">В 1955 году в школе появляется  первый автокружок под руководством Герасимова  Степана Семёновича. С 1962 года выпускники стали получать  водительские права.</p>
            <p class="read_more_target classic_paragraph">В 1959 году по инициативе директора школы  Главизина Иккентия Михайловича зародилась традиция – проводить вечера встречи с выпускниками школы. Первым в районе в 1960 году был открыт  в школе комсомольско-молодёжный лагерь.</p>
            <p class="read_more_target classic_paragraph">В 1962 году средняя школа из десятилетней была преобразована в одиннадцатилетнюю с производственным обучением.</p>
            <p class="read_more_target classic_paragraph">В 1965 году одним из первых в районе был проведён первый общешкольный  туристический слёт.</p>
            <p class="read_more_target classic_paragraph">Школа многие годы работала в две смены, так как было много учеников и все не помещались. У старшеклассников, учившихся во вторую смену, уроки заканчивались в 19 часов 10 минут.</p>
            <p class="read_more_target classic_paragraph">В 1987 году началось  строительство  нового трёхэтажного  здания  школы  на 624 места. Новая школа начала работать в  1988 году.</p>
            <p class="read_more_target classic_paragraph">Школа добилась многих успехов. Учителя и ученики участвуют в различных конкурсах и показывают неплохие результаты и в учёбе, и в спорте, и в культурных мероприятиях. Современная школа умело сочетает инновации и традиции.</p>
